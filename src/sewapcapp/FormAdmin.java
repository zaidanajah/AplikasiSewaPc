            String pass=""; //password database
            connector = (Connection) DriverManager.getConnection(url,user,pass);
            statSewa = (Statement) connector.createStatement(rs.TYPE_SCROLL_SENSITIVE,rs.CONCUR_UPDATABLE);
            rs = statSewa.executeQuery("select * from sewa_pc_table");
            
