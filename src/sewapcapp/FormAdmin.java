    private void table_databaseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_databaseMouseClicked
        try {
            Connector();
            int row =table_database.getSelectedRow();
            String tabel_klik=(table_database.getModel().getValueAt(row, 0).toString());
            java.sql.Statement stm = connector.createStatement();
            java.sql.ResultSet sql = stm.executeQuery("select * from sewa_pc_table where user_id='"+tabel_klik+"'");
            if(sql.next()){
                String id = sql.getString("user_id");
                field_userid.setText(id);
                String nama = sql.getString("Nama");
                field_nama.setText(nama);
                String waktu = sql.getString("Waktu");
                field_waktu.setText(waktu);
                String harga = sql.getString("Harga");
                field_harga.setText(harga);
            }
        } catch (Exception e) {
//            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_table_databaseMouseClicked

